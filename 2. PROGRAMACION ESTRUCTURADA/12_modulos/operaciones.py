'''
Operaciones basicas
'''
def suma(num1,num2):
    suma = 0
    suma = num1 + num2
    return suma

def resta(num1,num2):
    resta = 0
    resta = num1 - num2
    return resta

def multiplicacion(num1,num2):
    mult = 0
    mult = num1 * num2
    return mult

def division(num1,num2):
    div = 0
    div = num1 / num2
    return div